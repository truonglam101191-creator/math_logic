    return 1; // Success
  }

  // Get all categories
  Future<List<ACategory>> getAllCategories() async {
    return _categories.map((cat) => ACategory(cat)).toList();
  }

  // Get words for a specific category
  Future<List<AWord>> getWords(String category) async {
    final words = _wordsData[category] ?? [];
    return words.map((word) => AWord(category, word)).toList();
  }

  // Get total word count across all categories
  Future<int> getAllWordsCount() async {
    int total = 0;
    _wordsData.forEach((key, value) {
      total += value.length;
    });
    return total;
  }

  // Get category count
  Future<int> getCategoryCount() async {
    return _categories.length;
  }
