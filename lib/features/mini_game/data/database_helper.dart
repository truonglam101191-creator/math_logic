// Static data - No SQLite dependency needed

// Data constants
const String columnCategory = 'category';
const String columnWord = 'word';

// Data model class for categories
class ACategory {
  String category;
  String time;

  ACategory(this.category, {this.time = '00:00'});

  ACategory.fromMap(Map<dynamic, dynamic> map)
    : category = map[columnCategory],
      time = '00:00';

  Map<String, dynamic> toMap() {
    return {columnCategory: category};
  }
}

// Data model class for words
class AWord {
  String category;
  String word;
  bool isSuggeted = false;

  AWord(this.category, this.word, {this.isSuggeted = false});

  AWord.fromMap(Map<dynamic, dynamic> map)
    : category = map[columnCategory],
      word = map[columnWord];

  Map<String, dynamic> toMap() {
    return {columnCategory: category, columnWord: word};
  }
}

